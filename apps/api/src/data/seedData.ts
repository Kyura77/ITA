function buildBook(
  id: string,
  title: string,
  author: string,
  subject: "matematica" | "fisica" | "quimica" | "calculo" | "geral",
  phase: "base" | "aprofundamento",
  priority: "essencial" | "recomendado" | "opcional" = "recomendado",
  progressPercent = 0,
) {
  return {
    id,
    title,
    author,
    subject,
    phase,
    priority,
    progressPercent,
    status: computeBookStatus(progressPercent),
    notes: "",
    defaultAnkiDeck: `ITA::${subject.charAt(0).toUpperCase() + subject.slice(1)}`,
    recommendedOrder: null,
  };
}

