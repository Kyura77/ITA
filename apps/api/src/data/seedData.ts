function buildTopic(
  subject: "matematica" | "fisica" | "quimica" | "calculo",
  area: string,
  topic: string,
  yearPlan: 1 | 2 | 3,
  priorityIta: "baixa" | "media" | "alta" | "critica" = "media",
  extras: Partial<{
    subtopic: string;
    forOlympiad: boolean;
    notes: string;
    status: "nao_iniciado" | "em_andamento" | "base_concluida" | "aprofundando" | "dominado";
  }> = {},
) {
  const refs = bookRefs[subject];
  return {
    id: slugify(`${subject}-${yearPlan}-${area}-${topic}-${extras.subtopic ?? ""}`),
    subject,
    area,
    topic,
    subtopic: extras.subtopic ?? null,
    bookBaseId: refs.base,
    bookAdvancedId: refs.advanced,
    status: extras.status ?? "nao_iniciado",
    yearPlan,
    priorityIta,
    startDate: null,
    lastReviewedAt: null,
    totalStudyMinutes: 0,
    forOlympiad: extras.forOlympiad ?? false,
    ankiDeck: `ITA::${subject.charAt(0).toUpperCase() + subject.slice(1)}::${area}`,
    notes: extras.notes ?? null,
    prerequisites: null,
  };
}

